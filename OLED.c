/**
 * @brief Swap Number
 * 
 * Swap Number using call by reference method
 *
 * @param *a: First Number
 * @param *b: Second Number
 */
static void swap_num( uint16_t *a, uint16_t *b )
{
  uint16_t temp = *a;
  *a = *b;
  *b = temp;
}
